public class patterns_printing {
    public static void main(String[] args) {
        for(int i =0;i<6;i++){ //PRINT ROWS
            for(int j=0;j<=i;j++){ //PRINT * IN ROWS
                System.out.print("*");
            }
            System.out.println();
        }
    }
}







